// Q7: Write a function that takes a number as input and determines if it is between 1 and 10.
function isBetweenOneAndTen(num) {
  if (num >= 1 && num <= 10) {
    console.log("Number is between 1 and 10");
  } else {
    console.log("Number is not between 1 and 10");
  }
}

// Example
isBetweenOneAndTen(5); // Number is between 1 and 10