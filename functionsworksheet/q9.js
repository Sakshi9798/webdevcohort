// Q9: Write a function that takes a number as input and determines if it is a multiple of 3.
function isMultipleOfThree(num) {
  if (num % 3 === 0) {
    console.log("Number is a multiple of 3");
  } else {
    console.log("Number is not a multiple of 3");
  }
}

// Example
isMultipleOfThree(9); // Number is a multiple of 3