// Q6: Write a function that takes a string as input and determines if it is longer than 5 characters.
function isLongerThanFive(str) {
  if (str.length > 5) {
    console.log("String is longer than 5 characters");
  } else {
    console.log("String is 5 or fewer characters long");
  }
}

// Example
isLongerThanFive("Hello!"); // String is longer than 5 characters