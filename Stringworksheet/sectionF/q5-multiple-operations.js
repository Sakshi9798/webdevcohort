// Given a string: "JavaScript is Awesome", convert it to:
// a. All lowercase
// b. All uppercase
// c. Replace "Awesome" with "Powerful"
// d. Check if the string starts with "Java"

let str = "JavaScript is Awesome";
console.log(str.toLowerCase());           // a
console.log(str.toUpperCase());           // b
console.log(str.replace("Awesome", "Powerful")); // c
console.log(str.startsWith("Java"));      // d
